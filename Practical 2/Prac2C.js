let add = (a,b) => {
    return a + b;
};
let sub = (a,b) => {
    return a - b;
};
let mul = (a,b) => {
    return a * b;
};
let div = (a,b) => {
    return a / b;
};
console.log("Addition = ", add(10, 20));
console.log("Subtraction = ", sub(10, 20));
console.log("Multiplication = ", mul(10, 20));
console.log("Division = ", div(10, 20));